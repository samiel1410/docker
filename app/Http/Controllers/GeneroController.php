     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $genero = new Genero;
        $genero->create($request->all());        
        $genero = Genero::all();
        return view('pages.creategener')->with('genero',$genero);
    }
